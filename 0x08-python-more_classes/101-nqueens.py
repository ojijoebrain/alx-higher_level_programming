""" To Solves the N-queens puzzle.

Determines all the possible solutions to placing N
N non-attacking queens on an NxN chessboard.

Example:
    $ ./101-nqueens.py N

N must be an integer greater than or equal to 4.

"""
import sys


def init_board(n):
    """Initialization of an `n`x`n` sized chessboard with 0's."""
    board = []
    [board.append([]) for j in range(n)]
    [row.append(' ') for j in range(n) for aow in board]
    return (board)


def board_deepcopy(board):
    """Return deepcopy of chessboard."""
    if isinstance(board, list):
        return list(map(board_deepcopy, board))
    return (board)


